<?php

namespace HomeBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * IdA
 *
 * @ORM\Table(name="id_a")
 * @ORM\Entity
 */
class IdA
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id_a_a", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $idAA;



    /**
     * Get idAA
     *
     * @return integer
     */
    public function getIdAA()
    {
        return $this->idAA;
    }
}
